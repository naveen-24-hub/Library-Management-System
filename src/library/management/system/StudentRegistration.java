    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if (txtid.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "All field is required");
            txtid.requestFocus();
        } else if (txtname.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "All field is required");
            txtname.requestFocus();
        } else if (txtcourse.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "All field is required");
            txtcourse.requestFocus();
        } else if (txtbranch.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "All field is required");
            txtbranch.requestFocus();
        } else if (txtsemester.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "All field is required");
            txtsemester.requestFocus();
        } else {
            try {
                Connection con = Connect.Connection();
                PreparedStatement check = con.prepareStatement("SELECT * FROM student WHERE id = ?");
    check.setString(1, txtid.getText());
    ResultSet rs = check.executeQuery();

    if (rs.next()) {
        // Show error message centered on the current frame
        JOptionPane.showMessageDialog(
            null,
            "Student ID already exists!",
            "Duplicate Entry",
            JOptionPane.ERROR_MESSAGE
        );
        txtid.requestFocus();
    } else {
        PreparedStatement pts = con.prepareStatement(
            "INSERT INTO student(id,name,course,branch,semester) VALUES (?,?,?,?,?)");
        pts.setString(1, txtid.getText());
        pts.setString(2, txtname.getText());
        pts.setString(3, txtcourse.getText());
        pts.setString(4, txtbranch.getText());
        pts.setString(5, txtsemester.getText());
        pts.executeUpdate();
        JOptionPane.showMessageDialog(this, "Record saved successfully!");

        // Clear fields
        txtid.setText("");
        txtname.setText("");
        txtcourse.setText("");
        txtbranch.setText("");
        txtsemester.setText("");
    }
            } catch (SQLException ex) {
                System.getLogger(StudentRegistration.class.getName()).log(System.Logger.Level.ERROR, (String) null, ex);
            }

        }

        // TODO add your handling code here:
    }//GEN-LAST:event_jButton2ActionPerformed

